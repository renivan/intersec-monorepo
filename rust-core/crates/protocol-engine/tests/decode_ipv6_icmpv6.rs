use core_types::PacketRecord;
use protocol_engine::decode_packet;

#[test]
fn decodes_ipv6_icmpv6_packet() {
    let bytes = vec![
        // Ethernet
        0x00,0x11,0x22,0x33,0x44,0x55,
        0x66,0x77,0x88,0x99,0xAA,0xBB,
        0x86,0xDD,
        // IPv6
        0x60,0x00,0x00,0x00,
        0x00,0x04,
        0x3A, // next header = 58
        0x40,
        // src
        0x20,0x01,0x0D,0xB8,0x00,0x00,0x00,0x00,
        0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x01,
        // dst
        0x20,0x01,0x0D,0xB8,0x00,0x00,0x00,0x00,
        0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x02,
        // ICMPv6
        0x80,0x00,0x00,0x00,
    ];

    let record = PacketRecord {
        packet_number: 2,
        file_offset: 0,
        interface_id: Some(0),
        timestamp_epoch_micros: Some(2),
        captured_length: bytes.len() as u32,
        original_length: bytes.len() as u32,
        link_type: Some(1),
        raw_data: bytes,
        read_warnings: vec![],
    };

    let parsed = decode_packet(&record).unwrap();
    assert!(parsed.summary.contains("ICMPv6"));
}